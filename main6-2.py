from gpiozero import DigitalInputDevice   # 디지털 입력 장치를 다루기 위한 클래스 가져오기
from gpiozero import OutputDevice         # 출력 장치를 다루기 위한 클래스 가져오기
import time                               # 시간 지연(sleep) 기능을 쓰기 위해 time 모듈 가져오기


# -----------------------------------------------------------
# 부저 설정
# -----------------------------------------------------------
# GPIO 18번 핀에 연결된 능동 부저를 출력 장치로 설정한다.
# OutputDevice는 ON/OFF 형태로 제어되는 장치에 사용한다.
# bz.on()  -> 부저 켜기
# bz.off() -> 부저 끄기
bz = OutputDevice(18)


# -----------------------------------------------------------
# 가스 센서 설정
# -----------------------------------------------------------
# GPIO 17번 핀에 연결된 MQ-2 가스 센서의 D0(디지털 출력) 신호를 입력 장치로 설정한다.
# DigitalInputDevice는 센서가 0 또는 1 형태의 디지털 값을 보낼 때 사용한다.
# gas.value 값은 현재 센서 상태를 의미한다.
gas = DigitalInputDevice(17)


try:
    # 무한 반복문
    # 프로그램이 실행되는 동안 계속해서 센서 값을 확인한다.
    while True:

        # ---------------------------------------------------
        # 가스 감지 여부 확인
        # ---------------------------------------------------
        # MQ-2 모듈의 D0 핀은 보통 임계값을 넘으면 LOW(0) 신호를 내보낸다.
        # 따라서 gas.value == 0 이면
        # "가스가 감지된 상태"라고 판단한다.
        if gas.value == 0:
            print("GAS")   # 터미널 창에 가스 감지 메시지 출력
            bz.on()               # 부저를 켜서 사용자에게 위험 상황 알림

        else:
            # gas.value가 1이면 현재는 정상 상태로 본다.
            # 즉, 센서가 설정된 임계값 이상의 가스를 감지하지 않은 상태이다.
            print("OK")         # 터미널 창에 정상 상태 메시지 출력
            bz.off()              # 부저를 꺼서 소리가 나지 않도록 함

        # ---------------------------------------------------
        # 반복 확인 간격 설정
        # ---------------------------------------------------
        # 센서 값을 너무 빠르게 계속 읽으면 불필요하게 CPU를 많이 사용할 수 있으므로
        # 0.2초마다 한 번씩 상태를 확인하도록 잠시 대기한다.
        time.sleep(0.2)

# 사용자가 실행 중 Ctrl + C를 누르면 KeyboardInterrupt 예외가 발생한다.
# 이 예외를 처리해서 프로그램이 갑자기 에러 메시지를 띄우며 끝나지 않게 한다.
except KeyboardInterrupt:
    pass


# -----------------------------------------------------------
# 프로그램 종료 전 정리 작업
# -----------------------------------------------------------
# 반복문이 끝나고 프로그램이 종료될 때
# 혹시 부저가 켜진 상태로 남아 있을 수 있으므로 반드시 꺼준다.
bz.off()